#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_print_program_name(int argc, char **argv)
{
	ft_putstr(argv[0]);
}

int main (int argc, char **argv)
{
	ft_print_program_name(argc, argv);
	return (0);
}

